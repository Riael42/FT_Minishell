#include "minishell.h"

int	ft_isalpha(char *s)
{
	int		i;
	int		ret;
	char	c;

	ret = 1;
	i = 0;
	while (s[i] != '\0')
	{
		c = s[i];
		ret *= ((1 * (c >= 'a' && c <= 'z')) + (1 * (c >= 'A' && c <= 'Z')));
		i += 1;
	}
	return (ret);
}

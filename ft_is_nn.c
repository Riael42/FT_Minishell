#include "minishell.h"

int	ft_is_nn(char *str)
{
	int	i;

	i = 1;
	if (str == NULL)
		return (1);
	if (str[0] != '-')
		return (1);
	while (str[i] != '\0')
	{
		if (str[i] != 'n')
			return (1);
		i += 1;
	}
	return (0);
}
